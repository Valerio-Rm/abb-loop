DROP TABLE IF EXISTS application_data.lk_db_corrispondenze_inventory CASCADE;
CREATE TABLE IF NOT EXISTS application_data.lk_db_corrispondenze_inventory (
    inventory_corr_id   bigint GENERATED BY DEFAULT AS IDENTITY (
        INCREMENT BY 100 MINVALUE 1 MAXVALUE 9223372036854775807 START 100 CACHE 1
    ) NOT NULL,
    plant_id            bigint NOT NULL,
    distinta            varchar(500) NOT NULL,
    descrizione         text NULL,
    premontato          varchar(500) NULL,
    descrizione1        text NULL,
    linea               varchar(500) NULL,
    ripetizioni         varchar(100) NULL,
    magazzino           varchar(255) NULL,
    sheet_last          varchar(200) NULL,
    valid_from_day_id   numeric(8) NULL,
    valid_to_day_id     numeric(8) NULL,
