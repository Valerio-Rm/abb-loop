-- ==============================================================
-- Script: 12_migration_inventory_db_corrispondenze.sql
-- Purpose: Inventory "db corrispondenze" — semaphore, staging, snapshot,
--          lookup merge, audit, sync + snapshot purge.
-- Aligns with Liquibase-style single-file change set (change 12).
--
-- Prerequisites:
--   - application_staging.etl_monitoring, dblink, application_data.log_error_write_cfg
--
-- FDW inventory (expected ~3 new remote tables, schema TBD): add in a later migration when definitions exist.
--
-- Not in this file: application_staging.etl_run_inventory (KPI) — future change set.
-- ==============================================================

-- ----------------------------------------------------------------
-- 1) Semaphore: one row per SharePoint / Java ingestion run
-- ----------------------------------------------------------------
DROP TABLE IF EXISTS application_staging.inventory_ingestion_run CASCADE;
CREATE TABLE IF NOT EXISTS application_staging.inventory_ingestion_run (
    run_id              uuid PRIMARY KEY,
    plant_id            bigint NOT NULL,
    overall_status      varchar(40) NOT NULL DEFAULT 'OPEN',
    created_ts_utc      timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    updated_ts_utc      timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    source_file_hint    varchar(500) NULL,
    notes               text NULL,
    CONSTRAINT chk_inventory_ingestion_run_status CHECK (
        overall_status IN (
            'OPEN', 'LOADING', 'READY_FOR_SYNC', 'SYNCING', 'SYNC_DONE', 'FAILED', 'SYNC_FAILED'
        )
    )
);

CREATE INDEX IF NOT EXISTS idx_inventory_ingestion_run_plant_status
    ON application_staging.inventory_ingestion_run (plant_id, overall_status);

COMMENT ON TABLE application_staging.inventory_ingestion_run IS
    'Java/SharePoint ingestion gate: overall_status transitions; READY_FOR_SYNC when all sheets SUCCESS before DB sync.';

-- ----------------------------------------------------------------
-- 2) Per-sheet status for all-or-nothing gate (typically 4 sheets)
-- ----------------------------------------------------------------
DROP TABLE IF EXISTS application_staging.inventory_ingestion_run_sheet;
CREATE TABLE IF NOT EXISTS application_staging.inventory_ingestion_run_sheet (
    run_id          uuid NOT NULL REFERENCES application_staging.inventory_ingestion_run (run_id) ON DELETE CASCADE,
    sheet_name      varchar(200) NOT NULL,
    sheet_status    varchar(20) NOT NULL DEFAULT 'PENDING',
    row_count       integer NOT NULL DEFAULT 0,
    last_error      text NULL,
    updated_ts_utc  timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    CONSTRAINT pk_inventory_ingestion_run_sheet PRIMARY KEY (run_id, sheet_name),
    CONSTRAINT chk_inventory_ingestion_run_sheet_status CHECK (
        sheet_status IN ('PENDING', 'RUNNING', 'SUCCESS', 'FAILED')
    )
);

CREATE INDEX IF NOT EXISTS idx_inventory_ingestion_run_sheet_run
    ON application_staging.inventory_ingestion_run_sheet (run_id);

COMMENT ON TABLE application_staging.inventory_ingestion_run_sheet IS
    'Per-sheet ingestion outcome; ETL sync requires all expected sheets SUCCESS and run READY_FOR_SYNC.';

-- ----------------------------------------------------------------
-- 3) Staging — full workbook rows (union of sheets), keyed by run_id
--    Column set matches SharePoint workbook (db corrispondenze.xlsx).
-- ----------------------------------------------------------------
DROP TABLE IF EXISTS application_staging.db_corrispondenze_inventory;
CREATE TABLE IF NOT EXISTS application_staging.db_corrispondenze_inventory (
    staging_row_id      bigint GENERATED BY DEFAULT AS IDENTITY (
        INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1
    ) NOT NULL,
    run_id              uuid NOT NULL,
    plant_id            bigint NOT NULL,
    sheet               varchar(200) NOT NULL,
    distinta            varchar(500) NOT NULL,
    descrizione         text NULL,
    premontato          varchar(500) NULL,
    descrizione1        text NULL,
    linea               varchar(500) NULL,
    ripetizioni         varchar(100) NULL,
    magazzino           varchar(255) NULL,
    valid_from_day_id   numeric(8) NULL,
    valid_to_day_id     numeric(8) NULL,
    CONSTRAINT pk_db_corrispondenze_inventory PRIMARY KEY (staging_row_id)
);

CREATE INDEX IF NOT EXISTS idx_db_corrispondenze_inventory_run_plant
    ON application_staging.db_corrispondenze_inventory (run_id, plant_id);
CREATE INDEX IF NOT EXISTS idx_db_corrispondenze_inventory_run_sheet
    ON application_staging.db_corrispondenze_inventory (run_id, sheet);

COMMENT ON TABLE application_staging.db_corrispondenze_inventory IS
    'Staging: full daily workbook load per run_id (all sheets). Java inserts; sync moves to snapshot and merges into LK.';

-- ----------------------------------------------------------------
-- 4) Snapshot — archival copy 1:1 after successful LK sync
-- ----------------------------------------------------------------
DROP TABLE IF EXISTS application_staging.db_corrispondenze_inventory_snapshot;
CREATE TABLE IF NOT EXISTS application_staging.db_corrispondenze_inventory_snapshot (
    snapshot_row_id     bigint GENERATED BY DEFAULT AS IDENTITY (
        INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1
    ) NOT NULL,
    run_id              uuid NOT NULL,
    plant_id            bigint NOT NULL,
    snapshot_day_id     numeric(8) NOT NULL,
    loaded_ts_utc       timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    sheet               varchar(200) NOT NULL,
    distinta            varchar(500) NOT NULL,
    descrizione         text NULL,
    premontato          varchar(500) NULL,
    descrizione1        text NULL,
    linea               varchar(500) NULL,
    ripetizioni         varchar(100) NULL,
    magazzino           varchar(255) NULL,
    valid_from_day_id   numeric(8) NULL,
    valid_to_day_id     numeric(8) NULL,
    CONSTRAINT pk_db_corrispondenze_inventory_snapshot PRIMARY KEY (snapshot_row_id)
);

CREATE INDEX IF NOT EXISTS idx_db_corr_snap_run_plant
    ON application_staging.db_corrispondenze_inventory_snapshot (run_id, plant_id);
CREATE INDEX IF NOT EXISTS idx_db_corr_snap_purge
    ON application_staging.db_corrispondenze_inventory_snapshot (loaded_ts_utc);
CREATE INDEX IF NOT EXISTS idx_db_corr_snap_plant_day
    ON application_staging.db_corrispondenze_inventory_snapshot (plant_id, snapshot_day_id);

COMMENT ON TABLE application_staging.db_corrispondenze_inventory_snapshot IS
    'Historical copies of workbook rows after successful LK sync; retention via purge_db_corrispondenze_inventory_snapshot_older_than.';

-- ----------------------------------------------------------------
-- 5) Lookup (application_data) — merged state; soft-delete for removals
