-- ----------------------------------------------------------------
-- 0) Inventory source placeholder
-- ----------------------------------------------------------------
CREATE SCHEMA IF NOT EXISTS inventory_remote;
-- NOTE:
-- The inventory FDW source tables (MCBA, OpenBacklog, ProductionLineData)
-- are not created in this migration because the remote inventory schema is
-- not yet available. This change set provides staging/fact structures and
-- calculation procedures so the flow can be activated later.

-- ----------------------------------------------------------------
-- 1) Run semaphore and source status
-- ----------------------------------------------------------------
CREATE TABLE IF NOT EXISTS application_staging.inventory_kpi_run (
    run_id                 uuid PRIMARY KEY,
    plant_id               bigint NOT NULL,
    overall_status         varchar(40) NOT NULL DEFAULT 'OPEN',
    run_date_id            numeric(8) NULL,
    mcba_rows              bigint NOT NULL DEFAULT 0,
    open_backlog_rows      bigint NOT NULL DEFAULT 0,
    production_line_rows   bigint NOT NULL DEFAULT 0,
    validation_status      varchar(30) NOT NULL DEFAULT 'PENDING',
    note_msg               text NULL,
    created_ts_utc         timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    updated_ts_utc         timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    created_by             varchar NOT NULL,
    last_user              varchar NOT NULL,
    CONSTRAINT chk_inventory_kpi_run_status CHECK (
        overall_status IN ('OPEN', 'SYNCING', 'READY_FOR_CALC', 'CALCULATING', 'SUCCESS', 'FAILED')
    ),
    CONSTRAINT chk_inventory_kpi_run_validation CHECK (
        validation_status IN ('PENDING', 'VALID', 'PARTIAL', 'FAILED')
    )
);

CREATE INDEX IF NOT EXISTS idx_inventory_kpi_run_plant_status
    ON application_staging.inventory_kpi_run (plant_id, overall_status);

CREATE TABLE IF NOT EXISTS application_staging.inventory_kpi_run_source (
    run_id              uuid NOT NULL REFERENCES application_staging.inventory_kpi_run(run_id) ON DELETE CASCADE,
    source_name         varchar(100) NOT NULL,
    source_status       varchar(20) NOT NULL DEFAULT 'PENDING',
    row_count           bigint NOT NULL DEFAULT 0,
    source_note         text NULL,
    updated_ts_utc      timestamp NOT NULL DEFAULT timezone('UTC', clock_timestamp()),
    CONSTRAINT pk_inventory_kpi_run_source PRIMARY KEY (run_id, source_name),
    CONSTRAINT chk_inventory_kpi_run_source_status CHECK (
        source_status IN ('PENDING', 'RUNNING', 'SUCCESS', 'FAILED')
    )
);

