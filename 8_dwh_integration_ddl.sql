-- -----------------------------------------------------
-- lk_code (da [sp203_an].[Code])
-- Nota: CodeType non viene importato, solo code_type_source_id per tracciabilità
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS application_data.lk_code (
    code_id int8 GENERATED BY DEFAULT AS IDENTITY(INCREMENT BY 100 START 100) NOT NULL,
    code_code varchar(255) NOT NULL,
    code_ds varchar(500) NULL,
    plant_id int8 NOT NULL,
    line_id int8 NOT NULL,                  -- FK → lk_line (Codes are per-line)
    source_id int4 NULL,                    -- CodeIntId originale
    code_type_source_id int4 NULL,          -- CodeTypeIntId originale (solo per tracciabilità)
    source_codetype_key varchar(255) NULL,  -- CodeTypeKey da [sp203_an].[CodeType]
    source_codetype_description text NULL,  -- Description da [sp203_an].[CodeType]
    is_active bool DEFAULT true NOT NULL,
    is_deleted bool DEFAULT false NOT NULL,
    creation_ts timestamp DEFAULT timezone('utc', CURRENT_TIMESTAMP) NOT NULL,
    creator_user varchar NOT NULL,
    last_modified timestamp DEFAULT timezone('utc', CURRENT_TIMESTAMP) NULL,
    last_user varchar NOT NULL,
    CONSTRAINT pk_lk_code PRIMARY KEY (code_id),
    CONSTRAINT uk_lk_code UNIQUE (plant_id, line_id, code_code, code_type_source_id)
);
-- Indice su source_id per performance; non più univoco perché i source_id possono coincidere tra DWH
CREATE INDEX IF NOT EXISTS lk_code_source_idx 
  ON application_data.lk_code (line_id, source_id) 
  WHERE source_id IS NOT NULL;

COMMENT ON TABLE application_data.lk_code IS 'Codici (es. codici scarto/difetto) - origine: [sp203_an].[Code]';


