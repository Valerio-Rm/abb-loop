-- ================================================================
-- lk_production_time_date (da [sp203_an].[ProductionTimeDate])
-- Calendario produzione DWH: una riga per data PER LINEA.
-- SourceId non è globalmente univoco tra sorgenti, quindi l'univocità è line-scoped.
-- ================================================================
CREATE TABLE IF NOT EXISTS application_data.lk_production_time_date (
    production_time_date_id int8 GENERATED BY DEFAULT AS IDENTITY(INCREMENT BY 1 START 1) NOT NULL,
    line_id int8 NOT NULL,                  -- FK → lk_line (line-scoped calendar key)
    source_id int4 NOT NULL,                -- DWH ProductionTimeDate.Id (univoco nel perimetro line_id)
    production_date date NOT NULL,          -- Data di produzione
    id_day numeric(8) NOT NULL,              -- YYYYMMDD per join con lk_date
    year_num int4 NOT NULL,
    month_num int4 NOT NULL,
    day_num int4 NOT NULL,
    weekday_name varchar(50) NULL,
    week_number int4 NULL,
    quarter_num int4 NULL,
    CONSTRAINT pk_lk_production_time_date PRIMARY KEY (production_time_date_id),
    CONSTRAINT uk_lk_production_time_date_source UNIQUE (line_id, source_id),
    CONSTRAINT uk_lk_production_time_date_id_day UNIQUE (line_id, id_day)
);

COMMENT ON TABLE application_data.lk_production_time_date IS 'Calendario date produzione da DWH per linea - origine: [sp203_an].[ProductionTimeDate].';


-- ================================================================
-- PARTE 3: FACT TABLE ft_rawdata
-- (dati da DeviceOnStepGrouped, snowflake)
-- Partizionamento: LIST(plant_id) poi RANGE(day_id) mensile (come ft_attendance).
-- Usare create_plant_partition + create_monthly_partition da 4_create_function_procedure.sql.
-- ================================================================

CREATE TABLE IF NOT EXISTS application_data.ft_rawdata (
    rawdata_id int8 GENERATED BY DEFAULT AS IDENTITY(INCREMENT BY 1 START 1) NOT NULL,
    
    -- Chiavi SORGENTE (per tracciabilità e lookup verso DWH)
    source_date_id int4 NOT NULL,           -- ProductionTimeDateId originale
    source_code_id int4 NULL,               -- CodeIntId originale
    source_fixture_id int4 NOT NULL,        -- FixtureIntId originale
    source_result_id int4 NULL,             -- ResultIntId originale
    source_site_id int2 NOT NULL,           -- SiteIntId originale
    source_step_id int4 NOT NULL,           -- StepIntId originale
    source_shift_id int4 NOT NULL,          -- ShiftIntId originale
    pass_number int2 NOT NULL,              -- PassNumber (non serve mappatura)
    
    -- Chiavi TARGET (FK verso tabelle del sistema, generate da sequence)
    day_id numeric(8) NOT NULL,             -- YYYYMMDD per join con lk_date
    plant_id int8 NOT NULL,                 -- FK → lk_plant
    line_id int8 NOT NULL,                  -- FK → lk_line
    machine_id int8 NOT NULL,               -- FK → lk_machine (step)
    component_id int8 NULL,                 -- FK → lk_component (machine DWH) - tramite fixture
    fixture_id int8 NOT NULL,               -- FK → lk_fixture
    code_id int8 NULL,                      -- FK → lk_code
    result_id int8 NULL,                    -- FK → lk_result
    shift_dwh_id int8 NOT NULL,             -- FK → lk_shift_dwh
    process_key text NULL,                  -- Process.ProcessKey (line/process scope for BY_PROCESS)
    is_redundant boolean DEFAULT false NOT NULL, -- True when source machine is mapped to multiple lines
    redundancy_rank int4 DEFAULT 1 NOT NULL, -- Deterministic rank in redundant groups (1 for non-redundant)
    
    -- Metriche
    total_production int8 NOT NULL,
    cycle_time_avg_sec numeric(12,3) NULL,
    work_time_avg_sec numeric(12,3) NULL,
    work_time_total_sec numeric(12,3) NULL,
    
    -- Audit
    etl_batch_id varchar(50) NULL,          -- Identificativo batch ETL
    etl_loaded_ts timestamp DEFAULT timezone('utc', CURRENT_TIMESTAMP) NOT NULL,
    
    CONSTRAINT pk_ft_rawdata PRIMARY KEY (rawdata_id, plant_id, day_id)
) PARTITION BY LIST (plant_id);

-- Backward-compatible adjustments for existing environments
ALTER TABLE application_data.ft_rawdata
    ADD COLUMN IF NOT EXISTS process_key text;
ALTER TABLE application_data.ft_rawdata
    ADD COLUMN IF NOT EXISTS is_redundant boolean DEFAULT false NOT NULL;
ALTER TABLE application_data.ft_rawdata
    ADD COLUMN IF NOT EXISTS redundancy_rank int4 DEFAULT 1 NOT NULL;

-- Indici per performance query
CREATE INDEX IF NOT EXISTS ft_rawdata_day_idx ON application_data.ft_rawdata (day_id);
CREATE INDEX IF NOT EXISTS ft_rawdata_plant_line_idx ON application_data.ft_rawdata (plant_id, line_id);
CREATE INDEX IF NOT EXISTS ft_rawdata_machine_idx ON application_data.ft_rawdata (machine_id);
CREATE INDEX IF NOT EXISTS ft_rawdata_fixture_idx ON application_data.ft_rawdata (fixture_id);
CREATE INDEX IF NOT EXISTS ft_rawdata_shift_idx ON application_data.ft_rawdata (shift_dwh_id);

-- Recreate unique index with line/process scope for idempotent load keys
DROP INDEX IF EXISTS application_data.ft_rawdata_source_uidx;
CREATE UNIQUE INDEX IF NOT EXISTS ft_rawdata_source_uidx ON application_data.ft_rawdata (
    plant_id, line_id, day_id, source_date_id, source_step_id, source_fixture_id, 
    COALESCE(process_key, ''),
    source_shift_id, pass_number, COALESCE(source_code_id, -1), COALESCE(source_result_id, -1)
);

COMMENT ON TABLE application_data.ft_rawdata IS 'Fact table dati produzione - origine: [dbo].[DeviceOnStepGrouped]';


-- ================================================================
-- PARTE 3b: SUPPORT FACT TABLE ft_quality (FPY staging)
-- Tabella di appoggio per calcolo First Pass Yield:
-- - contiene conteggi per giorno con dettaglio (component, fixture, shift)
-- - NON contiene il KPI (rapporto), ma i componenti del calcolo (good/total first pass)
-- ================================================================
CREATE TABLE IF NOT EXISTS application_data.ft_quality (
    quality_id int8 GENERATED BY DEFAULT AS IDENTITY(INCREMENT BY 1 START 1) NOT NULL,
    plant_id int8 NOT NULL,
    line_id int8 NOT NULL,
    day_id numeric(8) NOT NULL,                 -- YYYYMMDD (match ft_kpi_target.target_date_iso)
    production_date date NOT NULL,              -- Data (derivata da day_id)
    shift_dwh_id int8 NOT NULL,                 -- FK → lk_shift_dwh
    machine_id int8 NOT NULL,                   -- FK → lk_machine (step)
    component_id int8 NULL,                     -- FK → lk_component
    fixture_id int8 NOT NULL,                   -- FK → lk_fixture
    good_first_pass int8 NOT NULL,              -- Pezzi buoni al primo passaggio (pass_number=1 AND lk_result.is_good=true)
    total_first_pass int8 NOT NULL,             -- Pezzi totali primo passaggio (pass_number=1)
    calc_ts timestamp DEFAULT timezone('utc', CURRENT_TIMESTAMP) NOT NULL,
    calc_caller varchar(255) NULL,
    CONSTRAINT pk_ft_quality PRIMARY KEY (quality_id, plant_id, day_id)
) PARTITION BY LIST (plant_id);

-- Unicità logica: una riga per combinazione (giorno, shift, step/component, fixture)
CREATE UNIQUE INDEX IF NOT EXISTS ft_quality_uidx ON application_data.ft_quality (
    plant_id, line_id, day_id, shift_dwh_id, machine_id, COALESCE(component_id, -1), fixture_id
);
CREATE INDEX IF NOT EXISTS ft_quality_day_idx ON application_data.ft_quality (day_id);
CREATE INDEX IF NOT EXISTS ft_quality_plant_line_idx ON application_data.ft_quality (plant_id, line_id);

COMMENT ON TABLE application_data.ft_quality IS 'Support fact per qualità/FPY: conteggi first-pass good/total per giorno con dettaglio component/fixture/shift.';


