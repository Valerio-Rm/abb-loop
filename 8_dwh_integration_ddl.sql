-- ================================================================
-- PARTE 6: TABELLA DI CONFIGURAZIONE SCHEMI SORGENTI DWH (FDW)
-- ---------------------------------------------------------------
-- Questa tabella configura quali schemi locali FDW (es. dwh_remote,
-- dwh_remote2, ...) rappresentano le diverse sorgenti DWH SQL Server
-- e permette alle procedure ETL di determinare dinamicamente quali
-- sorgenti processare (anche per gruppo logico).
-- ================================================================

CREATE SCHEMA IF NOT EXISTS application_staging AUTHORIZATION abb_prj_user;

CREATE TABLE IF NOT EXISTS application_staging.etl_source_schema_cfg (
    schema_name  TEXT PRIMARY KEY,                 -- es. 'dwh_remote', 'dwh_remote2'
    enabled      BOOLEAN NOT NULL DEFAULT TRUE,    -- se FALSE lo schema viene ignorato
    group_code   TEXT NULL,                        -- es. 'ALL', 'ABB_ONLY', 'ABB2_ONLY'
    run_order    INT  NOT NULL DEFAULT 0,          -- ordine di esecuzione
    creation_ts  TIMESTAMP DEFAULT timezone('utc', CURRENT_TIMESTAMP),
    creator_user VARCHAR  NOT NULL DEFAULT 'ETL_SYSTEM'
);

-- Valori iniziali: schemi FDW esistenti verso ABB e ABB2
INSERT INTO application_staging.etl_source_schema_cfg (schema_name, enabled, group_code, run_order)
VALUES
    ('dwh_remote',  TRUE, 'PROD', 10),
    ('dwh_remote2', TRUE, 'PROD', 20)
ON CONFLICT (schema_name) DO UPDATE
SET enabled   = EXCLUDED.enabled,
    group_code = EXCLUDED.group_code,
    run_order  = EXCLUDED.run_order;

