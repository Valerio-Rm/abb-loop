-- ================================================================
-- PARTE 6b: ETL rejected rows storage (strict mode support)
-- ---------------------------------------------------------------
-- Tabella di dettaglio per tracciare *quali* righe sorgente vengono
-- scartate (non caricate) dalle procedure ETL fact (strict mode),
-- con reason_code e chiavi sorgente in JSONB.
-- ================================================================
CREATE TABLE IF NOT EXISTS application_staging.etl_reject_row (
    reject_id      int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rejected_ts    timestamp NOT NULL DEFAULT timezone('UTC', current_timestamp),
    procedure_name varchar(100) NOT NULL,
    caller         varchar(100) NULL,
    batch_id       varchar(100) NULL,
    foreign_schema text NULL,
    target_date    date NULL,
    plant_id       int8 NULL,
    line_id        int8 NULL,
    process_key    text NULL,
    source_table   text NULL,
    reason_code    text NOT NULL,
    reason_detail  text NULL,
    source_key     jsonb NULL,
    source_payload jsonb NULL
);

CREATE INDEX IF NOT EXISTS etl_reject_row_batch_idx
    ON application_staging.etl_reject_row (procedure_name, batch_id);

CREATE INDEX IF NOT EXISTS etl_reject_row_target_idx
    ON application_staging.etl_reject_row (target_date, plant_id, line_id);

